function toggleDarkMode() {
    if (document.documentElement.classList.contains("light")) {
        document.documentElement.classList.remove("light")
        document.documentElement.classList.add("dark")
    } else if (document.documentElement.classList.contains("dark")) {
      document.documentElement.classList.remove("dark")
      document.documentElement.classList.add("light")
    } 
    
  }


function photoSave(clickedPhoto) {

    clickedPhotoSave = clickedPhoto;
    localStorage.setItem("photoNumber", clickedPhotoSave);
}

function photoView(){
    var clickedPhotoSave = localStorage.getItem("photoNumber");
    arrayPhotoPosition = clickedPhotoSave -1;
    
    allPhotos[arrayPhotoPosition].style.display = 'grid';

}


function navigatePhotos(direction) {
    if (direction == 'next' || direction == 'ArrowRight') {
        allPhotos[arrayPhotoPosition].style.display = 'none';
        arrayPhotoPosition++;

        if(arrayPhotoPosition > 34) {
            arrayPhotoPosition = 0;
        }

        allPhotos[arrayPhotoPosition].style.display = 'grid';
    }

    if (direction == 'back' || direction == 'ArrowLeft') {
        allPhotos[arrayPhotoPosition].style.display = 'none';
        arrayPhotoPosition--;

        if(arrayPhotoPosition < 0) {
            arrayPhotoPosition = 34;
        }

        allPhotos[arrayPhotoPosition].style.display = 'grid';
    }
}

